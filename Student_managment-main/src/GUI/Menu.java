    private void Delete_StudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Delete_StudentMouseClicked
        try {
            int selectedRow = BangSinhVien.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn sinh viên cần xóa", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String maSV = BangSinhVien.getValueAt(selectedRow, 0).toString();

            int confirm = JOptionPane.showConfirmDialog(this, 
                    "Bạn có chắc muốn xóa sinh viên này?? \n (Không thể hoàn tác)", 
                    "Xóa sinh viên", 
                    JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost/student_management", "root", "");
                
                // xoa tu bang sinhvien
                String deleteSinhVienQuery = "DELETE FROM sinhvien WHERE MASV = ?";
                PreparedStatement pstSinhVien = con.prepareStatement(deleteSinhVienQuery);
                pstSinhVien.setString(1, maSV);
                pstSinhVien.executeUpdate();
                
                // xoa data ben bang diem
                String deleteDiemQuery = "DELETE FROM diem WHERE MASV = ?";
                PreparedStatement pstDiem = con.prepareStatement(deleteDiemQuery);
                pstDiem.setString(1, maSV);
                pstDiem.executeUpdate();

                // xoa data khoi bang lop neu khong co sv nao khac tham chieu cung MALOP
                String checkOtherStudentsQuery = "SELECT COUNT(*) FROM sinhvien WHERE MALOP = (SELECT MALOP FROM sinhvien WHERE MASV = ?)";
                PreparedStatement pstCheck = con.prepareStatement(checkOtherStudentsQuery);
                pstCheck.setString(1, maSV);

                ResultSet rs = pstCheck.executeQuery();
                if (rs.next() && rs.getInt(1) <= 1) { // new chi co 1 hoac k co sv nao tham chieu MALOP
                    String deleteLopQuery = "DELETE FROM lop WHERE MALOP = (SELECT MALOP FROM sinhvien WHERE MASV = ?)";
                    PreparedStatement pstLop = con.prepareStatement(deleteLopQuery);
                    pstLop.setString(1, maSV);
                    pstLop.executeUpdate();
                }

                loadTableData_Student();
                clearTxtFields_Student();

                JOptionPane.showMessageDialog(this, "Xóa sinh viên thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
                con.close();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_Delete_StudentMouseClicked

