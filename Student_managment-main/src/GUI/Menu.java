            PreparedStatement pstDiem = con.prepareStatement(diemQuery);
            pstDiem.setString(1, txtMaSV.getText());
            pstDiem.executeUpdate();

            String malopQuery = "INSERT IGNORE INTO lop (MALOP) VALUES (?)";
            PreparedStatement pstMalop = con.prepareStatement(malopQuery);
            pstMalop.setString(1, txtMaLop.getText());
            pstMalop.executeUpdate();
            
            // add sinh vien
            String query = "INSERT INTO sinhvien (MASV, HOTEN, NGAYSINH, DIACHI, EMAIL, MALOP) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(query);

            pst.setString(1, txtMaSV.getText());
            pst.setString(2, txtHoTen.getText());

            String inputDate = txtNgaySinh.getText();
            SimpleDateFormat inputFormat = new SimpleDateFormat("dd/MM/yyyy");
            SimpleDateFormat dbFormat = new SimpleDateFormat("yyyy-MM-dd");
